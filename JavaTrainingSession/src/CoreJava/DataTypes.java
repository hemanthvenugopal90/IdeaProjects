package CoreJava;

public class DataTypes{
    public static void main(String[] args){
        //priitive data types int, double, char, boolean
        //int datatype
        int a=10;
        a=20;
        int b = 20;
        int c = 30;
        System.out.println(a);
       System.out.println(b+c);

      //Double Datatype
        double d = 12.33;
        double e = 14.55;
        System.out.println(d);
        System.out.println(e);

        //Char Datatype only single digit value, should be written within single quotes
        char c1 = 'a';
        char z = 'v';
        System.out.println(c1);
        System.out.println(z);

        //Boolean
        boolean b1=true;
        boolean b2=false;
        System.out.println(b1);
        System.out.println(b2);

        //String is a class , not a datatype
        String s = "Hemanth";
        String s1 = "Pavan";
        String N1 = "1000";
        System.out.println(s);
        System.out.println(s1);
        System.out.println(N1);
    }
}
